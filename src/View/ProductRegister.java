    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
       int choise=JOptionPane.showConfirmDialog(this, "Are you sure You want yo delete this Record","Delete Message", JOptionPane.YES_NO_OPTION);
       if(choise==JOptionPane.YES_OPTION){ 
           product_id.setEditable(false);
       Product pd=new Product();
         pd.setProduct_id(product_id.getText());
         ProductDao dao=new ProductDao();
         String feedback=dao.Deleteproduct(pd);
         JOptionPane.showMessageDialog(this, feedback); 
         product_id.setEditable(true);
         product_id.setText("");
         product_name.setText("");
         quantity.setText("");
         priceitem.setText("");
         daydate.setDate(null);
       }else{    
       }
    }//GEN-LAST:event_DeleteActionPerformed

