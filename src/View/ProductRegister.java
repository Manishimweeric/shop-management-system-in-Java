    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterActionPerformed
     if(product_id.getText().trim().isEmpty()||product_name.getText().trim().isEmpty()||quantity.getText().trim().isEmpty()||priceitem.getText().trim().isEmpty()){
         JOptionPane.showConfirmDialog(this, "Please Fill All Fields");
     }else{
         
         Product pd=new Product();
         pd.setProduct_id(product_id.getText());
         pd.setProduct_name(product_name.getText());
         pd.setQuantity(Integer.parseInt(quantity.getText()));
         pd.setPriceperItem(Integer.parseInt(priceitem.getText()));
         SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
         String todatdate= sdf.format(daydate.getDate()); 
         pd.setDatecames(todatdate);
         ProductDao dao=new ProductDao();
         String feedback=dao.Registerproduct(pd);
         JOptionPane.showMessageDialog(this, feedback); 
         
         product_id.setText("");
         product_name.setText("");
         quantity.setText("");
         priceitem.setText("");
         daydate.setDate(null);
     }
    }//GEN-LAST:event_RegisterActionPerformed
